export const WalletConnection = (props) => {

    const [web3,setWeb3] = useState(null)
    const [contract,setContract] = useState(null)
    const [currentAccount, setCurrentAccount] = useState(null);

  
  

    async function connectMetaMask(){
        const provider = await detectEthereumProvider(); 

            if(provider) { 

              const _web3 = new Web3(window.ethereum);
              setWeb3(_web3);
              let _contract = new _web3.eth.Contract(contractABI,contractAddress);
              console.log("Cars contract " , _contract);
              let accounts = await window.ethereum
              .request({ method: "eth_requestAccounts" })
              .catch((err)=>{
                if (err.code === 4001) {
                    // If this happens, the user rejected the connection request.
                    console.log('Please connect to MetaMask.'); 
                }

                else {
                    console.error(err);
                }                
              });

              let account = accounts[0];
              // contract = _contract;
              // currentAccount = account;
              setContract(_contract);
              setCurrentAccount(account);

              console.log("contract " , contract );
              console.log("currentAccount " , currentAccount);


              // check if multiple wallets & start app
              startApp(provider); 

              //Detect which network user is connected to
              const chainId = await provider.request({ 
                    method: 'eth_chainId'        
              })

              // If user changes network , reload page   
              ethereum.on('chainChanged', handleChainChanged); 

              //If user changes account
              ethereum.on('accountsChanged', handleAccountsChanged); 

            }
            
            else 
                console.log('Please install MetaMask!');
        

    }


    function startApp(provider){
        if (provider !== window.ethereum) {
            // Provide notification
            console.error('Do you have multiple wallets installed?'); 
        }

        // Access the decentralized app now
    }


    function handleChainChanged(_chainId) {
        window.location.reload();
      }
      


    function handleAccountsChanged(accounts) {
        if (accounts.length === 0) {
          // MetaMask is locked or the user has not connected any accounts
          console.log('Please connect to MetaMask.');
        } else if (accounts[0] !== currentAccount) {
          currentAccount = accounts[0];
         // setCurrentAccount(accounts[0])
          console.log("Now current account ", currentAccount);
         
        }
    }

  //   <CreateNFT
  //   contractInstance = {contract}
  //   currentConnectedAccount = {currentAccount}
  //  /> 

    return (
        <div>
                  <BootstrapButton variant="contained" onClick={connectMetaMask} disableRipple>
                      Connect MetaMask
                  </BootstrapButton>  

              
        </div>
    )
}
